class forEachLoop 
{
	public static void main(String[] args) 
	{
		int[]a={10,20,30,40,50};
		for(int i:a)
		System.out.println(i);
		System.out.println("***********");
		char[]b={'#','@','$','%'};
		for(char i:b)
		System.out.println(i);
		System.out.println("***********");
		String[]c={"java","JS","sql","python"};
		for(String i:c)
		System.out.println(i);
	}
}
