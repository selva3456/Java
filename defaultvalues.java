class defaultvalues 
{
	static byte a;
	static short b;
	static boolean c;
	static int d;
	static long e;
	static float f;
	static double g;
	static char h; 
	public static void main(String[] args) 
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
	}
}
